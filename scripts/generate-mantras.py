def yaml_block(text: str, indent: int = 6) -> str:
    pad = " " * indent
    lines = text.rstrip().split("\n")
    return "\n".join(pad + line for line in lines)


def assert_multiline(m: dict) -> None:
    lines = [ln.strip() for ln in m["sanskrit"].strip().split("\n") if ln.strip()]
    if len(lines) < 2:
        raise ValueError(f"{m['title']}: need at least 2 non-empty Sanskrit lines")


