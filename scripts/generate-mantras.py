def yaml_block(text: str, indent: int = 6) -> str:
    pad = " " * indent
    lines = text.rstrip().split("\n")
    return "\n".join(pad + line for line in lines)


def write_mantra(idx: int, m: dict) -> str:
    num = f"{idx:03d}"
    filename = f"{num}-{m['file']}.yaml"
    path = os.path.join(OUT, filename)

    source_block = ""
    if m.get("source"):
        source_block = f"""  pre_shlok: |
{yaml_block(m['source'])}
"""

    content = f"""title: {m['title']}
lyrics:
  sthayi_connect: false
{source_block}  paragraphs:
    - |
{yaml_block(m['sanskrit'])}
    - commentary: |
{yaml_block(m['meaning'])}
"""
    with open(path, "w", encoding="utf-8", newline="\n") as f:
        f.write(content)
    return filename


def main():
    os.makedirs(OUT, exist_ok=True)
    if len(MANTRAS) != 50:
        raise SystemExit(f"Expected 50 mantras, got {len(MANTRAS)}")
    written = []
    for i, m in enumerate(MANTRAS, start=1):
        written.append(write_mantra(i, m))
    print(f"Wrote {len(written)} files to {OUT}")
    for w in written:
        print(f"  {w}")


if __name__ == "__main__":
    main()
