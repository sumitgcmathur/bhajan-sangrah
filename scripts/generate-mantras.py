GENERATED_SLUGS = frozenset(m["file"] for m in MANTRAS)


def slug_from_name(name: str) -> str:
    m = re.match(r"^\d{3}-(.+)\.ya?ml$", name)
    return m.group(1) if m else name


def yaml_block(text: str, indent: int = 6) -> str:
    pad = " " * indent
    lines = text.rstrip().split("\n")
    return "\n".join(pad + line for line in lines)


def assert_multiline(m: dict) -> None:
    lines = [ln.strip() for ln in m["sanskrit"].strip().split("\n") if ln.strip()]
    if len(lines) < 2:
        raise ValueError(f"{m['title']}: need at least 2 non-empty Sanskrit lines")


def popularity_num(slug: str) -> int:
    try:
        return POPULARITY.index(slug) + 1
    except ValueError as e:
        raise ValueError(f"Slug not in POPULARITY: {slug}") from e


def write_mantra(m: dict) -> str:
    assert_multiline(m)
    num = f"{popularity_num(m['file']):03d}"
    filename = f"{num}-{m['file']}.yaml"
    path = os.path.join(OUT, filename)

    source_block = ""
    if m.get("source"):
        source_block = f"""  pre_shlok: |
{yaml_block(m['source'])}
"""

    content = f"""title: {m['title']}
lyrics:
  sthayi_connect: false
{source_block}  paragraphs:
    - |
{yaml_block(m['sanskrit'])}
    - commentary: |
{yaml_block(m['meaning'])}
"""
    with open(path, "w", encoding="utf-8", newline="\n") as f:
        f.write(content)
    return filename


def is_preserved_file(name: str) -> bool:
    return slug_from_name(name) in PRESERVED_SLUGS


def main():
    os.makedirs(OUT, exist_ok=True)
    if len(MANTRAS) != 20:
        raise SystemExit(f"Expected 20 mantras, got {len(MANTRAS)}")
    if GENERATED_SLUGS & PRESERVED_SLUGS:
        raise SystemExit("MANTRAS overlap with PRESERVED_SLUGS")

    for name in os.listdir(OUT):
        if not name.endswith((".yaml", ".yml")):
            continue
        slug = slug_from_name(name)
        if slug not in GENERATED_SLUGS:
            continue
        os.remove(os.path.join(OUT, name))

    for m in MANTRAS:
        write_mantra(m)


if __name__ == "__main__":
    main()
